package com.motivity;


public class Customer {
	
	private int id;
	private String name;
	private int fvid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getFvid() {
		return fvid;
	}
	public void setFvid(int fvid) {
		this.fvid = fvid;
	}
	
}
